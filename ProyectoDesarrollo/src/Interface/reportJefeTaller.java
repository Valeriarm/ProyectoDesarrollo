    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List valores = new ArrayList();
        String tiempo = "";
        if (String.valueOf(this.jComboBox1.getSelectedItem()) == "Inventario"){
            valores = bD.reporteInventario();
        }else if (String.valueOf(this.jComboBox1.getSelectedItem()) == "Orden de trabajo"){
            if(String.valueOf(this.jComboBox2.getSelectedItem()) == "Dia"){
                valores = bD.reporteOrdenesTrabajoDia(idJefe);
            } else if(String.valueOf(this.jComboBox2.getSelectedItem()) == "Mes"){
                valores = bD.reporteOrdenesTrabajoMes(idJefe);
            } else if(String.valueOf(this.jComboBox2.getSelectedItem()) == "Anio"){
                valores = bD.reporteOrdenesTrabajoAnio(idJefe);
            }
            
        }
        DefaultCategoryDataset dcd = new DefaultCategoryDataset();
        Report report = new Report("", 0);
        for (int i=0; i<valores.size(); i++){
            report = (Report) valores.get(i);
            dcd.setValue(report.getValue(), "Cantidad", report.getName());
        }
        
        JFreeChart jchart = ChartFactory.createBarChart("Student Record", 
                "Studen Name", "Studen Marks", dcd, PlotOrientation.VERTICAL, 
                true, true, false);
        
        ChartPanel chartPanel = new ChartPanel(jchart);
        reportPanel.removeAll();
        reportPanel.add(chartPanel);
        reportPanel.updateUI();
    }//GEN-LAST:event_jButton1ActionPerformed

